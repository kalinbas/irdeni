export type Language = 'de' | 'en' | 'es-mx'

export const DEFAULT_LANGUAGE: Language = 'de'

export function normalizeLanguageParam(value: string | null | undefined): Language {
  const normalized = (value ?? '').trim().toLowerCase()

  if (normalized === 'en' || normalized === 'en-us' || normalized === 'en_us') {
    return 'en'
  }

  if (normalized === 'es' || normalized === 'es-mx' || normalized === 'es_mx') {
    return 'es-mx'
  }

  return DEFAULT_LANGUAGE
}

export function readLanguageFromSearch(search?: string): Language {
  if (typeof window === 'undefined' && search === undefined) {
    return DEFAULT_LANGUAGE
  }

  const params = new URLSearchParams(search ?? window.location.search)
  return normalizeLanguageParam(params.get('lang'))
}

export function getCurrentLanguage() {
  return readLanguageFromSearch()
}

export function getEventFileName(language: Language) {
  if (language === 'en') {
    return 'events.en.json'
  }

  return language === 'es-mx' ? 'events.es-mx.json' : 'events.json'
}

export function getDocumentLanguage(language: Language) {
  if (language === 'en') {
    return 'en'
  }

  return language === 'es-mx' ? 'es-MX' : 'de'
}

